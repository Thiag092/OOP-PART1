
public class Multiplo {

    public static void main(String[] args) {

  

        System.out.println("Números múltiplos de 4 entre 42 y 150: ");

       

        for (int i = 42; i <= 150; i++) {

            if (i % 4 == 0) {

                System.out.println(i);

            }

        }

    }

}